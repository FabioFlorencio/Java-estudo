package cursojava.aula11;

public class CuriosidadeInt {
	
	public static void main(String[] args) {
		
		
		/*
		 	Os números no Java funcionam igual uma roleta. No momento que somar o limite,
		 	ele volta para os números negativos.
		 */
		
		int var1 = 2147483647;
		int var2 = 1;
		
		System.out.println(var1 + var2);
		
	}
}
